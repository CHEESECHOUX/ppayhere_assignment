def korea_chosung(text):
    result = []
    for ch in text:
        char_code = ord(ch) - 44032
        if char_code > -1 and char_code < 11172:
            idx = char_code // 28 // 21
            result.append(JA_LIST[idx])
        else:
            result.append(ch)
    return result
